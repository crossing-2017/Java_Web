package junit;

/**
 * 用来作为对junit的测试类
 * @author Crossing
 * @date 2019-03-25
 */
public class Calculator {


    /**
     * 加法
     * @param a
     * @param b
     * @return
     */
    public int add(int a, int b){
        return a + b;
    }

    /**
     * 减法
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b){
        return a - b;
    }
}
